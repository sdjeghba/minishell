#include "includes/libft.h"

char		*ft_strrev(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[j + 1] != '\0')
		j++;
	while (i < j)
		ft_swap(&str[i++], &str[j++]);
	return (str);
}
